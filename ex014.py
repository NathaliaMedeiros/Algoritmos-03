# Construir um programa que leia 3 valores inteiros e apresentar como resultado final o quadrado da soma dos 3

a = int(input('Digite o primeiro número: '))

b = int(input('Digite o segundo número: '))

c = int(input('Digite o terceiro número: '))

result = a + b + c

print(f'O quadrado da soma dos valores é {result * result}')

