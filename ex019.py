# Elaborar um programa que leia dois valores. Calcular e apresentar o resultado das 4 operções matemáticas básicas

x = int(input('Digite o primeiro valor: '))

y = int(input('Digite o segundo valor: '))

print(f'{x} + {y} = {x + y}')
print(f'{x} - {y} = {x - y}')
print(f'{x} * {y} = {x * y}')
print(f'{x} / {y} = {x / y}')

