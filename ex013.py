"""
Construir um programa que leia 3 valores inteiros e apresentar como resultado final o valor da soma dos quadrados dos 3
"""

a = int(input('Digite o primeiro número: '))

b = int(input('Digite o segundo número: '))

c = int(input('Digite o terceiro número: '))

print(f'A soma do quadrado dos números é {(a * a) + (b * b) + (c * c)}')
